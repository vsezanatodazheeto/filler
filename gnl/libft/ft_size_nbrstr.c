#include "libft.h"

unsigned int	ft_size_nbrstr(int n)
{
	unsigned int i;

	i = 0;
	if (n == 0)
	{
		i++;
		return (i);
	}
	if (n < 0)
		i++;
	while (n != 0)
	{
		n = n / 10;
		i++;
	}
	return (i);
}
